#include <iostream>
#include <string>

int main(int argc, char** argv)
{
	char c = 'c';

	switch(c)
	{
		case 'a': std::cout << c << std::endl; break;
		case 'b': std::cout << c << std::endl; break;
		case 'c': std::cout << c << std::endl; break;
		default: std::cout << c << std::endl; break;
	};	
		
}
